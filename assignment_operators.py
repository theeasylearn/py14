#concept of assignment operators in python 

a = 10
b = 20
print(f"a = {a} b = {b} ")

a+=b # a= a + b
print(f"a = {a} b = {b} ")

a-=b # a= a - b
print(f"a = {a} b = {b} ")

a*=b # a= a * b
print(f"a = {a} b = {b} ")

a/=b # a= a / b
print(f"a = {a} b = {b} ")

b=5 
a**=b # a= a ** b
print(f"a = {a} b = {b} ")

a//=b # a= a // b
print(f"a = {a} b = {b} ")