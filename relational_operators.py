#example of relational operators 
num1 = int(input("Enter first number"))
num2 = int(input("Enter second number"))
print(f"num1 = {num1} num2 = {num2}")

result = num1 == num2 
print(f"{num1} == {num2}  = {result}")

result = num1 != num2 
print(f"{num1} != {num2}  = {result}")

result = num1 < num2 
print(f"{num1} < {num2}  = {result}")

result = num1 > num2 
print(f"{num1} > {num2}  = {result}")

result = num1 <= num2 
print(f"{num1} <= {num2}  = {result}")

result = num1 >= num2 
print(f"{num1} >= {num2}  = {result}")