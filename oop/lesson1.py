#creating class (define class)
class Person:
    def eat(self):
        print('I can eat')
    def sleep(self):
        print('I can sleep')
    def walk(self):
        print('I can walk')
        

#create object 
#object-name = class()
ram = Person()
ram.walk()
ram.sleep()
ram.eat()

krishna = Person()
krishna.walk()
krishna.sleep()
krishna.eat()
    
